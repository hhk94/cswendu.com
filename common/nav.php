<script>
	$('.nav-search-btn1').on('click',function(){
		let key = $('.nav-search-input1').val()
		search(key)
	})
	$('.nav-search-btn2').on('click',function(){
		let key = $('.nav-search-input2').val()
		search(key)
	})
	$(".width-1200-center").click(function(event){
	    event.stopPropagation();//阻止事件冒泡即可
	})
	$('.fix-search-body').click(function(){
		$(this).slideUp()
		$('.nav-search-input2').val("")
	})
	$('.close').click(function(){
		$('.fix-search-body').slideUp()
		$('.nav-search-input2').val("")
	})
	function search(key){
		let params = {
			method:'POST',
			url: "<?=$api_url?>"+"app/home_search" ,
			data :{
				app_class:'web',
				user_token:window.token,
				keyword:key
			},
			successfn:function(res){
				let jsonRes = $.parseJSON( res );
				if(jsonRes.state==1){
					$('.fix-search-body').slideDown()
					$('.nav-search-input2').val(key)
					if(jsonRes.content.ask_res.length==0&&jsonRes.content.news_res.length==0){
						$('.warn').slideDown()
					}else{
						$('.warn').slideUp()
					}
				}
				console_log(jsonRes)
				let content = jsonRes.content
				itemAskAdd(content.ask_res)
				itemNewsAdd(content.news_res)
			}
			
		}
		all.sendAjax(params)
	}
	function itemAskAdd(content){
		//列表清空
		$('.ask-list').html('')
		$.isArray(content)&&content.forEach((item,index)=>{
			let times = all.getTime(item.create_time)
			let html = `
			<a href="<?=$file_url?>detail/detail-community.php?route=nav&nav=ask&ask_question_id=${item.ask_question_id}" target="_blank" class="search-list-item">
				<h1 class="item-title">${item.ask_title}</h1>
				<div class="item-desc">${item.ask_description}</div>
				<div class="item-time"> <i class="fa fa-clock-o" aria-hidden="true"></i>${times}</div>
			</a>`
			$('.ask-list').append(html)
		})
	}
	function itemNewsAdd(content){
		//列表清空
		$('.news-list').html('')
		$.isArray(content)&&content.forEach((item,index)=>{
			let times = all.getTime(item.create_time)
			let html = `
			<a href="<?=$file_url?>/cswendu.com/detail/detail-information.php?route=nav&nav=kaoyanInformation&news_info_id=${item.news_info_id}" target="_blank" class="search-list-item">
				<h1 class="item-title">${item.title}</h1>
				<div class="item-desc">${item.summary}</div>
				<div class="item-time"> <i class="fa fa-clock-o" aria-hidden="true"></i>${times}</div>
			</a>`
			$('.news-list').append(html)
		})
	}
</script>

